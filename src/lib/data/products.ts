export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getFeaturedProducts(limit = 6): Product[] {
  // Prioritize designer-capsule and celestial-essentials; ai-concept-archive last
  const priority = products
    .filter((p) => p.isFeatured && p.tags.collectionCategory !== "ai-concept-archive")
    .slice(0, limit);
  if (priority.length >= limit) return priority;
  const archive = products
    .filter((p) => p.isFeatured && p.tags.collectionCategory === "ai-concept-archive")
    .slice(0, limit - priority.length);
  return [...priority, ...archive];
}
