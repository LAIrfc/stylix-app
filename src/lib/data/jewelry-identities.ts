export const MOOD_TO_IDENTITIES: Record<MoodId, string[]> = {
  dreamy: ["aurora", "moonlight", "bloom", "void"],
  calm: ["void", "ink", "moonlight", "tide"],
  elegant: ["monarch", "oracle", "prism", "ink"],
  bold: ["ember", "nova", "muse", "monarch"],
  natural: ["atlas", "purity", "bloom", "ink"],
  mysterious: ["velvet", "purity", "tide", "moonlight"],
};

export function getIdentityById(id: string): JewelryIdentity | undefined {
  return IDENTITIES.find((i) => i.id === id);
}

export function getIdentitiesForMood(mood: MoodId): JewelryIdentity[] {
  const ids = MOOD_TO_IDENTITIES[mood];
  return ids.map((id) => IDENTITIES.find((i) => i.id === id)!).filter(Boolean);
}

export function getSimilarIdentities(
  currentId: string,
  limit = 3
): JewelryIdentity[] {
  const current = getIdentityById(currentId);
  if (!current) return IDENTITIES.slice(0, limit);

  return IDENTITIES.filter((i) => i.id !== currentId)
    .sort((a, b) => {
      const aShared = a.code.split("").filter((c, idx) => c === current.code[idx]).length;
      const bShared = b.code.split("").filter((c, idx) => c === current.code[idx]).length;
      return bShared - aShared;
    })
    .slice(0, limit);
}
